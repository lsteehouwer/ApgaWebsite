<main>
    <h1>Spelregels</h1>
    <ul>
        <li>
            Elke student mag zich inschrijven met een username naar keuze. Eenmaal gekozen, mag deze naam niet veranderd worden tenzij daar een (hele) goede reden voor is.
        </li>
        <li>
            Elke challenge is optioneel.
        </li>
        <li>
            Een challenge niet inleveren is 0 punten voor die challenge.
        </li>
        <li>
            Voor elke challenge kan 30 punten worden verdiend. De challenge wordt hiervoor op de volgende punten beoordeeld:
            <ol>
                <li>
                    Verdieping - hoeveel moeite heb je gedaan om nuttige en/of interessante informatie te vinden? Is die informatie ook daadwerkelijk nuttig? Ben je wel volledig geweest?
                </li>
                <li>
                    Argumentatie - is je uiteenzetting logisch, en gebruik je je gevonden informatie goed?
                </li>
                <li>
                    Schrijfstijl - spelling, grammatica, en of het natuurlijk wel een beetje lekker leest.
                </li>
            </ol>
            <p>
            Je kunt in totaal 2 bonuspunten verdienen. Je krijgt de 2 bonuspunten als je een totaalscore hebt van 80% of meer van het maximaal aantal haalbare punten dat je bij de challenges kunt verdienen. <br/>
            Heb je minder gehaald dan 80%, dan kijken we naar je score in vergelijking met een score van 80%.
            </p>
            <p>
            Een voorbeeld: Als er in totaal 15 challenges zijn, dan kun je in totaal verdienen: 30 punten per challenge * 15 challenges = 450 punten. <br />
            80% van de maximale score van 450 punten = 360 punten. Heb je bijvoorbeeld in dit geval 360 punten of meer behaald bij het maken van challenges, dan heb je 2 bonuspunten verdiend bovenop het eindcijfer van de opdrachten.<br />
            Heb je echter 250 punten behaald dan krijg je in dit geval de volgende bonuspunten: 250/360*2 = 1,4 bonuspunten bovenop het eindcijfer van de opdrachten.
            </p>
        </li>
        <li>
            Als je een challenge te laat inlevert loop je het risico dat we je inlevering negeren of dat we punten eraf trekken.
        </li>
        <li>
            Wij sturen je <strong>geen</strong> e-mail als je ons een e-mail verstuurt zonder bijlage. Check dus goed <strong>voordat</strong> je inlevert of je je bestand wel hebt bijgevoegd.
        </li>
        <li>
            Scorefouten zijn voorbehouden en kunnen ten alle tijde gewijzigd worden (naar boven en beneden).
        </li>
    </ul>
</main>
